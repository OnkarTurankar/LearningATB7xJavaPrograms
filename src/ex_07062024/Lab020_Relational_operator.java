package ex_07062024;

public class Lab020_Relational_operator {
    public static void main(String[] args) {
// Relational operator -> boolean
        System.out.println(10>=10); //10>10 10=10
//        True false=true
//        True true=true
//        False true= true
//        false false=false
        System.out.println(10<=10); //true
        System.out.println(10==10); //comparison operator
        System.out.println(10<10);//false
        System.out.println(10>10);//false

    }
}
